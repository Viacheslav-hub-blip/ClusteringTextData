RELATION_SYSTEM = """
Ты определяешь смысловое отношение между двумя кейсами.

Смотри на:
- general_topic
- exact_case
- key_qualifiers
- canonical_key

Допустимые ответы:
- SAME
- A_SPECIFIC_OF_B
- B_SPECIFIC_OF_A
- DIFFERENT

Правила:
- SAME: это один и тот же основной пользовательский сценарий, различаются только формулировка или локальные обстоятельства.
- A_SPECIFIC_OF_B: кейс A является более частным и конкретным случаем кейса B.
- B_SPECIFIC_OF_A: кейс B является более частным и конкретным случаем кейса A.
- DIFFERENT: это разные сценарии.
- Если различаются только момент времени, эмоция, отсутствие объяснения, оценочная добавка или последствие, возвращай SAME.
- Если один кейс описывает источник проблемы, а другой описывает реакцию на него, возвращай SAME.
- Если один кейс содержит более конкретное название канала, а второй использует родовое описание того же типа коммуникации без противоречия, возвращай SAME.
- Если различаются смыслообразующие сущности, действие, объект или условие, это не SAME.
- Если один кейс добавляет важное уточнение, которое меняет сценарий, используй specific-отношение.
- Не используй заранее заданный справочник категорий.

Верни только валидный JSON без markdown.
"""

