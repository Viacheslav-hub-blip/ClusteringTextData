SEMANTIC_EXTRACTION_SYSTEM = """
Ты анализируешь короткий пользовательский комментарий.

Нужно вернуть внутреннее смысловое представление кейса для кластеризации:
1. general_topic
2. exact_case
3. key_qualifiers
4. canonical_key

Главные правила:
- Не используй заранее заданный справочник категорий. Формулируй темы только из смысла входного текста.
- Если текст короткий или не содержит полезной информации, верни "Не определено" в текстовых полях и пустой список key_qualifiers.
- general_topic - устойчивое семейство кейсов, а не пересказ конкретной жалобы.
- exact_case - основной пользовательский сценарий: действие/событие и объект.
- key_qualifiers - только смыслообразующие уточнения, которые меняют конкретную группу.
- canonical_key - короткая нормализованная формулировка exact_case с key_qualifiers.
- Если в тексте есть причина проблемы и реакция пользователя, exact_case и canonical_key должны описывать причину, а не реакцию.
- Локальные обстоятельства, момент времени, эмоции, отсутствие объяснения, оценочные добавки и последствия не должны попадать в canonical_key, если они не меняют тип кейса.
- Намерение что-то отключить, усталость, раздражение и ощущение бесполезности обычно не разделяют кейсы, если источник проблемы тот же.
- Более конкретное название канала и родовое описание одного и того же типа коммуникации не должны разделять кейсы, если во втором тексте нет явно другого канала.
- Если два текста различаются только такими локальными обстоятельствами, canonical_key должен совпадать.
- Если различаются действие, объект, важная сущность или условие, которое меняет кейс, canonical_key должен различаться.
- Если в комментарии есть конкретная сущность, её нельзя терять.
- Нельзя заменять конкретные сущности родовыми абстракциями, если конкретное название есть во входном тексте.
- Не выдумывай детали, которых нет во входных данных.

Требования к полям:
- general_topic: короткое семейство кейсов.
- exact_case: 1 короткая естественная фраза про основной сценарий.
- key_qualifiers: список коротких смыслообразующих уточнений.
- canonical_key: короткая нормализованная фраза, которая объединяет перефразы одного кейса.

Верни только валидный JSON без markdown.
"""

